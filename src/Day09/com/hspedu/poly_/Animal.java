package Day09.com.hspedu.poly_;

public class Animal {//父类，先来个构造器，然后setter and getter
    private String name;

    public Animal(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
